        filterItems('admix');

        // Función de filtrado
        $('.filtrar').click(function(){
            $filter = $(this).data('filter');
            filterItems($filter);
        });

        // Función para filtrar los elementos
        function filterItems(filter) {
            if(filter == 'all'){
                $('.tile').fadeIn();
            } else {
                $('.tile').hide();
                $('.tile[data-category="'+ filter + '"]').fadeIn();
            }
        }
    });
</script> 
